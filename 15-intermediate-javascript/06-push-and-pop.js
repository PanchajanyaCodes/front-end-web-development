// Array Techniques.

// Removing last element from the array.
var fruits = ["Pineapple", "Kiwi", "Mango", "Guava", "Banana"];
poppedItem = fruits.pop();

console.log("Popped Array:", fruits);
console.log("Popped Item:", poppedItem);

// Adding element to the end of an array.
fruits.push("Dragon Fruit");
console.log("Pushed Array:", fruits);
